# Função II
# Para retornar valores dentro de uma função
# Basta utilizar a palavra reservada "return"
def somar(n1, n2):
    return n1 + n2

# Na chamada da função, após a sua execução
# o retorno assume o lugar da função
resultado = somar(5, 9)
print(resultado)

# Printando o Retorno Diretamente
print(somar(4, 12))